package vn.com.huy.a128.model;

public class Box {

	private int x;
	private int y;
	private int value;
	private boolean isSun;
	
	public int getX() {
		return x;
	}

	public void setX(int x) {
		this.x = x;
	}

	public int getY() {
		return y;
	}

	public void setY(int y) {
		this.y = y;
	}

	public int getValue() {
		return value;
	}

	public void setValue(int value) {
		this.value = value;
	}

	public boolean isSun() {
		return isSun;
	}

	public void setSun(boolean isSun) {
		this.isSun = isSun;
	}
	
	

}
